import SwiftUI

// View extension for monospaced font compatibility
extension View {
    @available(iOS 17, *)
    func monospaced(_ state: Bool) -> some View {
        if state {
            return AnyView(self.font(.system(.body, design: .monospaced)))
        }
        return AnyView(self)
    }
}
